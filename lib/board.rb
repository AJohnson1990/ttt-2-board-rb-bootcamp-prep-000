row=["","",""]
# position 1: left
row[0]="x"

# position 2: Middle
row[1]="x"

# position 3: Right

row[2]

# move X to the position 2, Middle
row[1]="X"
row #=> ["","X",""]

# move X to the position 3, Right
row[2]="X"
row #=> ["","","X"]

# move x back to position 0, left
row[0]="x"
row #=> ["x","",""]
